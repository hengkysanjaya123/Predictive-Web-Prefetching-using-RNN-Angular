const predict = async (path, country) => {
  const pageIdx = pages.indexOf(path);
  const countryIdx = countries.indexOf(country);

  console.log('calling API for predicting: path: ' + path + ' and country:' + country);
  console.log();
  // call the API for prediction
  fetch('http://localhost:5000/api/v1/prediction?country_id=' + countryIdx + '&path=' + pageIdx).then((res) => {
    return res.json();
  }).then((predicted) => {
    console.log(`Navigating from: '${path}' (${country})`);
    console.log('Top 3 predictions: ', predicted);

    for (let p of predicted) {
      prefetch(path, country, p);
    }
  })

  return '';
};

const connectionSpeeds = {"slow-2g": 0.001, "2g": 0.00001, "3g": 0, "4g": 0};

const prefetch = async (path, country, predictedInfo) => {
  const connectionSpeed = navigator.connection.effectiveType;
  const threshold = connectionSpeeds[connectionSpeed];
  const cache = await caches.open(ImageCache);

  if (predictedInfo.probability >= threshold) {
    console.log('prefetching path: ' + predictedInfo.path + ' (' + predictedInfo.probability + ')');
    const pathInfo = paths.find(pt => pt.originalPath === predictedInfo.path);
    if (pathInfo.images !== undefined && pathInfo.images.length > 0) {
      pathInfo.images.forEach((imageUrl) => {
        // console.log(imageUrl);
        const request = new Request(imageUrl, {
          mode: "no-cors",
        });
        fetch(request).then((response) => {
          cache.put(request, response);
          console.log('storing image to cache: ' + imageUrl);
        });
      });
    }
  }
};


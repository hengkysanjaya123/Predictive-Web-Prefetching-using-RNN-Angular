const ImageCache = "image-cache";

self.addEventListener("fetch", (event) => {
  event.respondWith(
    caches.open(ImageCache).then((cache) => {
      return cache.match(event.request).then((response) => {
        return (
          response ||
          fetch(event.request).then((response) => {
            return response;
          })
        );
      });
    })
  );
});

